# ჩვენ გვაქვს რამდენიმე შედარების ოპერატორი Python-ში,ესენია: ==, !=, <, >, <=, >=
# თითოეული მათგანი სხვადასხვაგვარად მუშაობს და გამოიყენება სხვადასხვა სიტუაციებში.
# == ოპერატორი გამოიყენება ორი მნიშვნელობის ტოლობის შესამოწმებლად.
print(5 == 5)  # True
print(5 == 3)  # False
print("hello" == "hello")  # True
print("hello" == "world")  # False
print(3.14 == 3.14)  # True
# != ოპერატორი გამოიყენება ორი მნიშვნელობის უთანასწორობის შესამოწმებლად.
print(5 != 3)  # True
print(5 != 5)  # False
print("hello" != "world")  # True
print("hello" != "hello")  # False
print(3.14 != 2.71)  # True
# < ოპერატორი გამოიყენება იმის შესამოწმებლად თუ მარჯვენა მნიშვნელობა მეტია მარცხენაზე.
print(3 < 5)  # True
print(5 < 3)  # False
print(2 < 2)  # False
print(1.5 < 2.5)  # True
print("apple" < "banana")  # True
# > ოპერატორი გამოიყენება იმის შესამოწმებლად თუ მარცხენა მნიშვნელობა მეტია მარჯვნაზე.
print(5 > 3)  # True
print(3 > 5)  # False
print(2 > 2)  # False
print(2.5 > 1.5)  # True
print("banana" > "apple")  # True
# <= ოპერატორი გამოიყენება იმის შესამოწმებლად თუ მარცხენა მნიშვნელობა ნაკლებია ან ტოლია მარჯვნაზე.
print(3 <= 5)  # True
print(5 <= 5)  # True
print(5 <= 3)  # False
print(2.5 <= 3.5)  # True
print("apple" <= "banana")  # True
# >= ოპერატორი გამოიყენება იმის შესამოწმებლად თუ მარცხენა მნიშვნელობა მეტია ან ტოლია მარჯვნაზე.
print(5 >= 3)  # True
print(5 >= 5)  # True
print(3 >= 5)  # False
print(3.5 >= 2.5)  # True
print("banana" >= "apple")  # True
# ამ ოპერატორების გამოყენება საშუალებას გვაძლევს შევადაროთ სხვადასხვა ტიპის მონაცემები და მივიღოთ შედეგები True ან False.ამ შედეგების ტიპს boolean ეწოდება.
# ჩვენ ასევე გვაქვს ლოგიკური ოპერატორები რომლებიცაა: and და or.ისინიც გამოიყენება boolean მნიშვნელობებთან მუშაობისთვის.
# and ოპერატორის გამოყენებისას თუ გვინდა რომ შედეგი იყოს True,მაშინ ორივე პირობა უნდა იყოს True.
print((5 > 3) and (2 < 4))  # True
print((5 > 3) and (2 > 4))  # False
print((5 < 3) and (2 < 4))  # False
# თუ and ლოგიკური ოპერატორის გამოყენებისას ერთ-ერთი პირობა მაინც არის False,შედეგი იქნება False.
# or ოპერატორის გამოყენებისას თუ გვინდა რომ შედეგი იყოს True,მაშინ საკმარისია რომ ერთი პირობა იყოს True.
print((5 > 3) or (2 < 4))  # True
print((5 < 3) or (2 < 4))  # True
print((5 < 3) or (2 > 4))  # False
# or ლოგიკური ოპერატორის გამოყენებისას False შედეგი მიიღება მხოლოდ მაშინ,როდესაც ორივე პირობა არის False.
# გამოვიყენოთ შედარების ოპერატორი > რომ შევადაროთ მომხმარებლის მიერ შემოტანილი რიცხვი ჩვენ მიერ არჩეულ რიცხვს.
ChosenNumber = 15
UserInput = int(input("please enter a number: "))
print(UserInput > ChosenNumber)
# თუ მომხმარებლის მიერ შემოტანილი რიცხვი მეტია 15-ზე,შედეგი იქნება True,ხოლო თუ ის ნაკლებია ან ტოლია 15-ისა,შედეგი იქნება False.
# ახლა მომხმარებელს შემოვატანინოთ სახელი და შევადაროთ ის წინასწარ განსაზღვრულ სახელს.
OurChosenName = "Andria"
UsersChosenName= input("please enter a name: ")
print(UsersChosenName == OurChosenName)
# თუ მომხმარებლის მიერ შემოტანილი სახელი ემთხვევა "Andria"-ს,შედეგი იქნება True,თუ მომხმარებელმა შემოიტანა სხვა ნებისმიერი სახელი შედეგი იქნება False.
# ახლა კი შევამოწმოთ მომხმარებლის ასაკი მეტია თუ არა 18-ზე.
LegalAge = 18
UserAge = int(input("please enter your age: "))
print(UserAge > LegalAge)
# თუ მომხმარებლის ასაკი მეტია 18-ზე,შედეგი იქნება True,თუ ის ნაკლებია ან ტოლია 18-ისა,შედეგი იქნება False.
