print(type(3.14))
# თუ ჩვენ შევიყვანთ ათწილად რიცხვს მაშინ პროგრამა დაბეჭდავს ასეთ ტექსტს:<class 'float'>
print(type(True))
# თუ ჩვენ შევიყვანთ True ან False მაშინ პროგრამა დაბეჭდავს ასეთ ტექსტს:<class 'bool'>



# data conversion-ეს არის მონაცემის ტიპის შეცვლა.ჩვენ შეგვიძლია integer ანუ რიცხვი ვაქციოთ string ად
# ანუ ტექსტად ან პირიქით string ად ჩაწერილი რიცხვი ვაქციოთ integer ანუ რიცხვად.
# ამაში გვეხმარება შემდეგი ფუნქციები:str(), int(), float(), bool().
# ვაქციოთ რიცხვი ტექსტად:
print(str(100))
print(str(134))
