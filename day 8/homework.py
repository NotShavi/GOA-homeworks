# type-როგორც ვიცით გვაქვს მონაცემთა სხვადასხვა ტიპები, მაგალითად: int, float, str, bool და ა.შ.
# type ფუნქცია კი გვეხმარება გავიგოთ რომელი ტიპის მონაცემი გვაქვს მოცემული.
