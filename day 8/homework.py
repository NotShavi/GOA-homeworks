# ამ შემთხვევაში პროგრამა დაბეჭდავს 100,134,3572 როგორც string ებს.
# ვაქციოთ ტექსტად ჩაწერილი რიცხვი რიცხვად:
print(int("200"))
print(int("450"))
print(int("7890"))
# ამ შემთხვევაში პროგრამა დაბეჭდავს 200,450,7890 როგორც integer ებს.
# ვაქციოთ ტექსტად ჩაწერილი ათწილად რიცხვი ათწილად რიცხვად:
print(float("3.14"))
print(float("2.71"))
print(float("1.618"))
# ამ შემთხვევაში პროგრამა დაბეჭდავს 3.14,2.71,1.618 როგორც float ებს.
# ვაქციოთ რიცხვი ათწილად რიცხვად:
print(float(10))
print(float(25))
print(float(50))
# ამ შემთხვევაში პროგრამა დაბეჭდავს 10.0,25.0,50.0 როგორც float ებს.
# ვაქციოთ რიცხვი boolean ტიპად:
print(bool(1))
print(bool(0))
print(bool(-5))
# ამ შემთხვევაში პროგრამა დაბეჭდავს True,False,True რადგან 1 და -5 ითვლება True როგორც boolean ტიპში.
# ვაქციოთ ტექსტად ჩაწერილი რიცხვი boolean ტიპად:
print(bool("Hello"))
print(bool(""))
print(bool("0"))
